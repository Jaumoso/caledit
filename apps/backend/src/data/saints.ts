export function getSaintsForMonth(month: number): { day: number; name: string }[] {
  const result: { day: number; name: string }[] = []
  for (const [key, name] of Object.entries(SAINTS)) {
    const [m, d] = key.split('-').map(Number)
    if (m === month) {
      result.push({ day: d, name })
    }
  }
  return result.sort((a, b) => a.day - b.day)
}

export function getSaint(month: number, day: number): string | null {
  return SAINTS[`${month}-${day}`] || null
}
